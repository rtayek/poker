	static Rank[] inverseLookup(int[][] a,int n) {
		Rank[] ranks=new Rank[5];
		for(int c=0;c<5;c++)
			for(int r=12;r>=0;r--)
				if (a[r][c]<=n) {
					n-=a[r][c];
					ranks[c]=Rank.fromInt(r+Rank.deuce.ordinal());
					break;
				}
		return ranks;
	}
