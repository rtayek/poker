	static void sortDescending(Rank[] rank) {
		for(int i=0;i<rank.length-1;i++)
			for(int j=i+1;j<rank.length;j++)
				if (rank[i].ordinal()<rank[j].ordinal()) {
					Rank temp=rank[i];
					rank[i]=rank[j];
					rank[j]=temp;
				}
	}
	public static Card[] toCanonicalForm(Rank[] ranks,boolean areSuited) {
		toCanonicalForm(ranks);
		final Card[] cards=new Card[ranks.length];
		for(int i=0;i<ranks.length;i++)
			cards[i]=Card.instance(ranks[i],!areSuited&i==0?diamonds:clubs);
		return cards;
	}
